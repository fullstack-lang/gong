// MultiCodeGeneratorNgSorting parses mdlPkg and generates the code for the
// Sorting component
func MultiCodeGeneratorNgSorting(
	mdlPkg *ModelPkg,
	pkgName string,
	matTargetPath string,
	pkgGoPath string) {

	// have alphabetical order generation
	structList := []*GongStruct{}
	for _, _struct := range mdlPkg.GongStructs {
		structList = append(structList, _struct)
	}
	sort.Slice(structList[:], func(i, j int) bool {
		return structList[i].Name < structList[j].Name
	})

	for _, _struct := range mdlPkg.GongStructs {
		if !_struct.HasNameField() {
			continue
		}

		// create the component directory
		dirPath := filepath.Join(matTargetPath, strings.ToLower(_struct.Name)+"s-sorting")
		errd := os.Mkdir(dirPath, os.ModePerm)
		if os.IsNotExist(errd) {
			log.Println("creating directory : " + dirPath)
		}

		// generate the css file
		VerySimpleCodeGenerator(mdlPkg,
			pkgName,
			pkgGoPath,
			filepath.Join(dirPath, strings.ToLower(_struct.Name)+"s-sorting.component.css"),
			NgFileSortingCssTmpl,
		)

		// generate the typescript file
		codeTS := NgSortingTemplateTS

		TsInsertions := make(map[NgSortingTsInsertionPoint]string)
		for insertion := NgSortingTsInsertionPoint(0); insertion < NgSortingTsInsertionsNb; insertion++ {
			TsInsertions[insertion] = ""
		}

		HtmlInsertions := make(map[NgSortingHtmlInsertionPoint]string)
		for insertion := NgSortingHtmlInsertionPoint(0); insertion < NgSortingHtmlInsertionsNb; insertion++ {
			HtmlInsertions[insertion] = ""
		}

		codeHTML := NgSortingTemplateHTML

		for _, field := range _struct.Fields {
			switch field.(type) {
			case *GongBasicField:
				gongBasicField := field.(*GongBasicField)

				// conversion form go type to ts type
				TypeInput := ""
				switch gongBasicField.basicKind {
				case types.Int, types.Float64:
					TypeInput = "type=\"number\" [ngModelOptions]=\"{standalone: true}\" "
				case types.String:
					TypeInput = "name=\"\" [ngModelOptions]=\"{standalone: true}\"     "
				}

				switch gongBasicField.basicKind {
				case types.Float64:
					HtmlInsertions[NgSortingHtmlInsertionColumn] += Replace2(NgSortingHTMLSubTemplateCode[NgSortingHTMLBasicFloat64Field],
						"{{FieldName}}", gongBasicField.Name,
						"{{TypeInput}}", TypeInput)
				case types.Bool:
					HtmlInsertions[NgSortingHtmlInsertionColumn] += Replace2(NgSortingHTMLSubTemplateCode[NgSortingHTMLBool],
						"{{FieldName}}", gongBasicField.Name,
						"{{TypeInput}}", TypeInput)
				default:

					if gongBasicField.DeclaredType != "time.Duration" {
						HtmlInsertions[NgSortingHtmlInsertionColumn] += Replace2(NgSortingHTMLSubTemplateCode[NgSortingHTMLBasicField],
							"{{FieldName}}", gongBasicField.Name,
							"{{TypeInput}}", TypeInput)
					} else {
						HtmlInsertions[NgSortingHtmlInsertionColumn] += Replace1(NgSortingHTMLSubTemplateCode[NgSortingHTMLBasicFieldTimeDuration],
							"{{FieldName}}", gongBasicField.Name)
					}
				}
				TsInsertions[NgSortingTsInsertionPerStructColumns] +=
					Replace1(NgSortinglSubTemplateCode[NgSortingTSPerStructColumn],
						"{{FieldName}}", gongBasicField.Name)

				if gongBasicField.DeclaredType == "time.Duration" {
					TsInsertions[NgSortingTsInsertionPerStructRecoveries] +=
						Replace1(NgSortinglSubTemplateCode[NgSortingTSPerStructTimeDurationRecoveries],
							"{{FieldName}}", gongBasicField.Name)
				}
			case *GongTimeField:
				gongTimeField := field.(*GongTimeField)
				TsInsertions[NgSortingTsInsertionPerStructColumns] +=
					Replace1(NgSortinglSubTemplateCode[NgSortingTSPerStructColumn],
						"{{FieldName}}", gongTimeField.Name)

				HtmlInsertions[NgSortingHtmlInsertionColumn] += Replace1(NgSortingHTMLSubTemplateCode[NgSortingHTMLTimeField],
					"{{FieldName}}", gongTimeField.Name)
			case *PointerToGongStructField:
				modelPointerToStructField := field.(*PointerToGongStructField)

				HtmlInsertions[NgSortingHtmlInsertionColumn] += Replace3(NgSortingHTMLSubTemplateCode[NgSortingPointerToStructHTMLFormField],
					"{{FieldName}}", modelPointerToStructField.Name,
					"{{AssocStructName}}", modelPointerToStructField.GongStruct.Name,
					"{{assocStructName}}", strings.ToLower(modelPointerToStructField.GongStruct.Name))

				TsInsertions[NgSortingTsInsertionPerStructColumns] +=
					Replace1(NgSortinglSubTemplateCode[NgSortingTSPerStructColumn],
						"{{FieldName}}", modelPointerToStructField.Name)
			}
		}

		//
		// Parse all fields from other structs that points to this struct
		//
		for _, __struct := range structList {
			for _, field := range __struct.Fields {
				switch field.(type) {
				case *SliceOfPointerToGongStructField:
					fieldSliceOfPointerToModel := field.(*SliceOfPointerToGongStructField)

					if fieldSliceOfPointerToModel.GongStruct == _struct {
						HtmlInsertions[NgSortingHtmlInsertionColumn] += Replace2(NgSortingHTMLSubTemplateCode[NgSortingPointerToSliceOfGongStructHTMLFormField],
							"{{FieldName}}", fieldSliceOfPointerToModel.Name,
							"{{AssocStructName}}", __struct.Name)

						TsInsertions[NgSortingTsInsertionPerStructColumns] +=
							Replace1(NgSortinglSubTemplateCode[NgSortingTSPerStructColumn],
								"{{FieldName}}", fieldSliceOfPointerToModel.Name)
					}
				}
			}
		}

		for insertion := NgSortingTsInsertionPoint(0); insertion < NgSortingTsInsertionsNb; insertion++ {
			toReplace := "{{" + string(rune(insertion)) + "}}"
			codeTS = strings.ReplaceAll(codeTS, toReplace, TsInsertions[insertion])
		}

		for insertion := NgSortingHtmlInsertionPoint(0); insertion < NgSortingHtmlInsertionsNb; insertion++ {
			toReplace := "{{" + string(rune(insertion)) + "}}"
			codeHTML = strings.ReplaceAll(codeHTML, toReplace, HtmlInsertions[insertion])
		}

		// final replacement
		codeTS = Replace6(codeTS,
			"{{PkgName}}", pkgName,
			"{{TitlePkgName}}", strings.Title(pkgName),
			"{{pkgname}}", strings.ToLower(pkgName),
			"{{PkgPathRoot}}", strings.ReplaceAll(pkgGoPath, "/models", ""),
			"{{Structname}}", _struct.Name,
			"{{structname}}", strings.ToLower(_struct.Name))
		codeHTML = Replace6(codeHTML,
			"{{PkgName}}", pkgName,
			"{{TitlePkgName}}", strings.Title(pkgName),
			"{{pkgname}}", strings.ToLower(pkgName),
			"{{PkgPathRoot}}", strings.ReplaceAll(pkgGoPath, "/models", ""),
			"{{Structname}}", _struct.Name,
			"{{structname}}", strings.ToLower(_struct.Name))
		{
			file, err := os.Create(filepath.Join(dirPath, strings.ToLower(_struct.Name)+"s-sorting.component.ts"))
			if err != nil {
				log.Panic(err)
			}
			defer file.Close()
			fmt.Fprint(file, codeTS)
		}
		{
			file, err := os.Create(filepath.Join(dirPath, strings.ToLower(_struct.Name)+"s-sorting.component.html"))
			if err != nil {
				log.Panic(err)
			}
			defer file.Close()
			fmt.Fprint(file, codeHTML)
		}

	}
}
