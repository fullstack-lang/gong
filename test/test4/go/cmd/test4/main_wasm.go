//go:build wasm

package main

import (
	"bytes"
	"context"
	"encoding/json"
	"flag"
	"fmt"
	"io"
	"log"
	"net/http/httptest"
	"syscall/js"

	"github.com/fullstack-lang/gong/test/test4/go/controllers"
	test4_models "github.com/fullstack-lang/gong/test/test4/go/models"
	"github.com/fullstack-lang/gong/test/test4/go/orm"
	"github.com/gin-gonic/gin"
)

var ginEngine *gin.Engine

func main() {
	flag.Parse()

	// 1. Initialize the exact same Gong app in memory
	// Capture the Gin router in a temporary variable 'r'
	r, stack := setupApp()

	// Assign it to our global variable so wasmFetch can use it!
	ginEngine = r

	fmt.Println("From the backend. after setupApp")

	// 2. Expose the HTTP and Socket bridges to the Angular frontend
	js.Global().Set("wasmFetch", js.FuncOf(wasmFetch))

	fmt.Println("From the backend. after wasmFetch")

	js.Global().Set("openWasmSocket", js.FuncOf(openWasmSocket))

	fmt.Println("From the backend. after openWasmSocket")

	err := test4_models.ParseAstEmbeddedFile(stack.Stage, stage_content, "data/stage.go")
	if err != nil {
		log.Fatalln(err.Error())
		fmt.Println("Error while parsing")
	} else {
		stack.Stage.Commit()
		fmt.Println("After the first commit")
	}

	// 3. Prevent the WASM module from exiting
	select {}
}

// wasmFetch intercepts HTTP calls and routes them through Gin (Task 4)
func wasmFetch(this js.Value, args []js.Value) any {

	fmt.Println("From the backend. in wasmFetch")

	reqJson := args[0].String()

	var reqData struct {
		Method string `json:"method"`
		URL    string `json:"url"`
		Body   string `json:"body"`
	}
	json.Unmarshal([]byte(reqJson), &reqData)

	fmt.Printf("From the backend, json unmarshall %v\n", reqData)

	var body io.Reader
	if reqData.Body != "" {
		body = bytes.NewBufferString(reqData.Body)
	}

	// Pass the fake request to the Gin Engine
	req := httptest.NewRequest(reqData.Method, reqData.URL, body)
	req.Header.Set("Content-Type", "application/json")
	w := httptest.NewRecorder()

	ginEngine.ServeHTTP(w, req)

	res := map[string]any{
		"status": w.Code,
		"body":   w.Body.String(),
	}

	jsRes, _ := json.Marshal(res)
	return string(jsRes)
}

// openWasmSocket mimics your WebSocket by subscribing directly to the BackRepo
func openWasmSocket(this js.Value, args []js.Value) any {
	stackPath := args[0].String()
	callback := args[1]

	fmt.Println("From the backend: openWasmSocket", stackPath)

	go func() {
		// Access the global controller generated by Gong
		ctrl := controllers.GetController()
		backRepo := ctrl.Map_BackRepos[stackPath]

		if backRepo == nil {
			fmt.Println("From the backend: ERROR - backRepo is nil for path:", stackPath)
			return
		}

		// Helper function to marshal and push the data
		pushStateToFrontend := func() {
			backRepoData := new(orm.BackRepoData)
			orm.CopyBackRepoToBackRepoData(backRepo, backRepoData)

			jsonData, err := json.Marshal(backRepoData)
			if err == nil {
				// Push to the Angular frontend via the JS callback
				callback.Invoke(string(jsonData))
			} else {
				fmt.Println("From the backend: ERROR marshaling JSON:", err)
			}
		}

		fmt.Println("From the backend: pushing initial state to new connection...")

		// 1. Instantly push the current state to the connecting frontend
		pushStateToFrontend()

		// 2. Subscribe to the channel to push any future commits
		ctx := context.Background()
		updateChannel := backRepo.SubscribeToCommitNb(ctx)

		// Loop exactly like the real WebSocket handler does
		for range updateChannel {
			fmt.Println("From the backend: openWasmSocket received update on the commit nb")
			pushStateToFrontend()
		}
	}()
	return nil
}
