//go:build wasm

package main

import (
	"bytes"
	"context"
	"encoding/json"
	"flag"
	"io"
	"net/http/httptest"
	"syscall/js"

	"github.com/fullstack-lang/gong/test/test4/go/controllers"
	"github.com/fullstack-lang/gong/test/test4/go/orm"
	"github.com/gin-gonic/gin"
)

var ginEngine *gin.Engine

func main() {
	flag.Parse()

	// 1. Initialize the exact same Gong app in memory
	ginEngine = setupApp()

	// 2. Expose the HTTP and Socket bridges to the Angular frontend
	js.Global().Set("wasmFetch", js.FuncOf(wasmFetch))
	js.Global().Set("openWasmSocket", js.FuncOf(openWasmSocket))

	// 3. Prevent the WASM module from exiting
	select {}
}

// wasmFetch intercepts HTTP calls and routes them through Gin (Task 4)
func wasmFetch(this js.Value, args []js.Value) any {
	reqJson := args[0].String()

	var reqData struct {
		Method string `json:"method"`
		URL    string `json:"url"`
		Body   string `json:"body"`
	}
	json.Unmarshal([]byte(reqJson), &reqData)

	var body io.Reader
	if reqData.Body != "" {
		body = bytes.NewBufferString(reqData.Body)
	}

	// Pass the fake request to the Gin Engine
	req := httptest.NewRequest(reqData.Method, reqData.URL, body)
	req.Header.Set("Content-Type", "application/json")
	w := httptest.NewRecorder()

	ginEngine.ServeHTTP(w, req)

	res := map[string]any{
		"status": w.Code,
		"body":   w.Body.String(),
	}

	jsRes, _ := json.Marshal(res)
	return string(jsRes)
}

// openWasmSocket mimics your WebSocket by subscribing directly to the BackRepo (Task 5)
func openWasmSocket(this js.Value, args []js.Value) any {
	stackPath := args[0].String()
	callback := args[1]

	go func() {
		// Access the global controller generated by Gong
		ctrl := controllers.GetController()
		backRepo := ctrl.Map_BackRepos[stackPath]

		if backRepo == nil {
			return
		}

		ctx := context.Background()
		updateChannel := backRepo.SubscribeToCommitNb(ctx)

		// Loop exactly like the real WebSocket handler does
		for range updateChannel {
			backRepoData := new(orm.BackRepoData)
			orm.CopyBackRepoToBackRepoData(backRepo, backRepoData)

			jsonData, err := json.Marshal(backRepoData)
			if err == nil {
				// Push to the Angular frontend via the JS callback
				callback.Invoke(string(jsonData))
			}
		}
	}()
	return nil
}
