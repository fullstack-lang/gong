//go:build !js

package main

import (
	"log"
	"os"

	"github.com/spf13/cobra"

	"github.com/fullstack-lang/gong/dsm/phylla/go/level1stack"
	"github.com/fullstack-lang/gong/dsm/phylla/go/models"
)

var export3dCmd = &cobra.Command{
	Use:   "export-3d [data/stage.go] [output_file.go]",
	Short: "Export the Three.js stage generated by a given data/stage.go",
	Args:  cobra.ExactArgs(2),
	Run: func(cmd *cobra.Command, args []string) {
		unmarshallFromCode := args[0]
		outputFile := args[1]

		log.Println("Reading stage from:", unmarshallFromCode)

		// Setup level1 stack
		stack := level1stack.NewLevel1Stack("phylla", unmarshallFromCode, "", true, embeddedDiagrams)

		// Initialize stager which enforces semantics and generates the Three.js object
		stager := models.NewStager(
			stack.R,
			stack.Stage,
			stack.Probe,
		)

		// Get the threejs stage and marshall it
		threejsStage := stager.GetThreejsStage()
		file, err := os.Create(outputFile)
		if err != nil {
			log.Fatalf("Error creating output file: %s", err.Error())
		}
		defer file.Close()

		threejsStage.Marshall(file, "github.com/fullstack-lang/gong/lib/threejs/go/models", "main")
		log.Println("Successfully exported Three.js stack to:", outputFile)
	},
}

func init() {
	rootCmd.AddCommand(export3dCmd)
}
